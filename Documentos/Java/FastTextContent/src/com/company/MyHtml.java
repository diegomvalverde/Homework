package com.company;

class MyHtml {
    private String Name;
    private String Html;
    private long Tiempo = 0;

    String getName() {
        return Name;
    }

    void setName(String name) {
        Name = name;
    }

    String getHtml() {
        return Html;
    }

    void setHtml(String html) {
        Html = html;
    }

    long getTiempo() {
        return Tiempo;
    }

    void setTiempo(long tiempo) {
        Tiempo = tiempo;
    }
}
